# ---------------------------------------------------------------------------
# Normals from SDF gradient (central differences)
# ---------------------------------------------------------------------------

def _gradient_normal(chunk: SDFChunk, i: int, j: int, k: int) -> tuple:
    """Estimate the SDF gradient at (i,j,k) using central differences."""
    R = chunk.resolution

    def _d(ii: int, jj: int, kk: int) -> float:
        ii = max(0, min(R - 1, ii))
        jj = max(0, min(R - 1, jj))
        kk = max(0, min(R - 1, kk))
        return chunk.get_d(ii, jj, kk)

    dx = _d(i + 1, j, k) - _d(i - 1, j, k)
    dy = _d(i, j + 1, k) - _d(i, j - 1, k)
    dz = _d(i, j, k + 1) - _d(i, j, k - 1)
    length = math.sqrt(dx * dx + dy * dy + dz * dz)
    if length < 1e-12:
        # Fall back to radial normal using the voxel world position
        px, py, pz = chunk.get_pos(i, j, k)
        length = math.sqrt(px * px + py * py + pz * pz)
        if length < 1e-12:
            return (0.0, 1.0, 0.0)
        return (px / length, py / length, pz / length)
    return (dx / length, dy / length, dz / length)


# ---------------------------------------------------------------------------
# IMesher protocol
# ---------------------------------------------------------------------------

class IMesher:
    """Duck-typed mesher interface.  Subclasses override ``build_mesh``."""

    def build_mesh(self, chunk: SDFChunk) -> Optional[Mesh]:  # pragma: no cover
        raise NotImplementedError


# ---------------------------------------------------------------------------
# MarchingCubesMesher
# ---------------------------------------------------------------------------

class MarchingCubesMesher(IMesher):
    """
    Standard Marching Cubes (Lorensen & Cline, 1987) isosurface extractor.

    A cube corner is considered *inside* (rock) when its SDF value is
    **negative**.  The isosurface is the d=0 level set.
    """

    def build_mesh(self, chunk: SDFChunk) -> Optional[Mesh]:
        """
        Extract the isosurface from *chunk* and return a Mesh, or None if
        the chunk is homogeneous (all air or all rock).
        """
        if chunk.is_empty() or chunk.is_full():
            return None

        R = chunk.resolution
        vertices: list = []
        normals:  list = []
        indices:  list = []

        # We cache edge-vertex indices to avoid duplicate vertices on shared edges.
        # Key: (cube_origin_flat, edge_id) → vertex list index
        edge_cache: dict = {}

        def _interp_vertex(
            ia: int, ja: int, ka: int,
            ib: int, jb: int, kb: int,
            da: float, db: float,
        ) -> int:
            """
            Interpolate an isosurface vertex on the edge between voxels A and B.
            Returns the index into *vertices* (may reuse a cached value).
            """
            # Canonical edge key (smaller flat index first)
            flat_a = chunk.flat_index(ia, ja, ka)
            flat_b = chunk.flat_index(ib, jb, kb)
            key    = (min(flat_a, flat_b), max(flat_a, flat_b))
            if key in edge_cache:
                return edge_cache[key]

            # Linear interpolation along the edge
            denom = da - db
            t     = 0.5 if abs(denom) < 1e-12 else da / denom
            t     = max(0.0, min(1.0, t))

            pxa, pya, pza = chunk.get_pos(ia, ja, ka)
            pxb, pyb, pzb = chunk.get_pos(ib, jb, kb)
            vx = pxa + t * (pxb - pxa)
            vy = pya + t * (pyb - pya)
            vz = pza + t * (pzb - pza)

            # Normal: interpolate gradient
            na = _gradient_normal(chunk, ia, ja, ka)
            nb = _gradient_normal(chunk, ib, jb, kb)
            nx = na[0] + t * (nb[0] - na[0])
            ny = na[1] + t * (nb[1] - na[1])
            nz = na[2] + t * (nb[2] - na[2])
            nl = math.sqrt(nx * nx + ny * ny + nz * nz)
            if nl > 1e-12:
                nx, ny, nz = nx / nl, ny / nl, nz / nl
            else:
                nx, ny, nz = 0.0, 1.0, 0.0

            idx = len(vertices)
            vertices.append((vx, vy, vz))
            normals.append((nx, ny, nz))
            edge_cache[key] = idx
            return idx

        # Iterate over all (R-1)³ cubes
        for ck in range(R - 1):
            for cj in range(R - 1):
                for ci in range(R - 1):
                    # Gather 8 corner SDF values
                    corners_d = [
                        chunk.get_d(ci + di, cj + dj, ck + dk)
                        for (di, dj, dk) in _CORNER_OFFSETS
                    ]

                    # Compute case index (bit k = 1 if corner k is inside/rock)
                    case_idx = 0
                    for bit, d in enumerate(corners_d):
                        if d < 0.0:
                            case_idx |= (1 << bit)

                    edge_mask = _EDGE_TABLE[case_idx]
                    if edge_mask == 0:
                        continue

                    # Compute edge vertices needed for this case
                    edge_verts: list = [None] * 12
                    for eid, (ca, cb) in enumerate(_EDGE_CORNERS):
                        if not (edge_mask & (1 << eid)):
                            continue
                        dia, dja, dka = _CORNER_OFFSETS[ca]
                        dib, djb, dkb = _CORNER_OFFSETS[cb]
                        edge_verts[eid] = _interp_vertex(
                            ci + dia, cj + dja, ck + dka,
                            ci + dib, cj + djb, ck + dkb,
                            corners_d[ca], corners_d[cb],
                        )

                    # Emit triangles
                    tris = _TRI_TABLE[case_idx]
                    pos  = 0
                    while pos < len(tris) and tris[pos] != -1:
                        a = edge_verts[tris[pos]]
                        b = edge_verts[tris[pos + 1]]
                        c = edge_verts[tris[pos + 2]]
                        indices += [a, b, c]
                        pos += 3

        if not indices:
            return None

        return Mesh(vertices=vertices, normals=normals, indices=indices)
